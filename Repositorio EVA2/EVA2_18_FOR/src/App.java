public class App {
    public static void main(String[] args) throws Exception {
        for(int i=1; i<=10; i++) {
            System.out.print(i+"-");
        }
        for(int i=10; i>=1; i--) {
            System.out.print(i+"-");
        }

        for(int i=0; i<=100; i+=2) {
            System.out.println(i+"-");
        }


    }
}
